# Python Rounding, Absolute Value, and Exponents

# round()
myGPA = 3.6
print(round(myGPA))

amountofSalt = 1.4
print(round(amountofSalt))

apple = -1.2
print(round(apple))
google = -1.6
print(round(google))

# abs()
distanceAway = -13
print(abs(distanceAway))
lengthOfRootInGround = -2.5
print(abs(lengthOfRootInGround))

# pow()
# 3^2 --> pow(3, 2) --> 9
changeOfTails = 0.5
inARowTails = 3
print(pow(changeOfTails, inARowTails))

changeOfOne = 0.167
inARowOne = 2
print(pow(changeOfOne, inARowOne))
