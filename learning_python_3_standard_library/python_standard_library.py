# What is standard library?
# A standard library is a library that can be used on any machine where you have an application using the
# specific language.
# Here our specific language is 'Python' and the standard library provides a bunch of functions that we can use
# in our code.
# The Python standard library provides two things: built-in functions and built-in, but imported modules.
# Buit-in funcitons are functions like "len()", "min()" and "max()"
# Built-in imported modules like "math" which is an imported module
# The Python standard library is availbale in any operation system.

# How is standard library different from an external library?
# A standard library is built-in
# An external library is something you need to download from the internet separately to get a special functionality
