# Built-in Function: type()
# The type() function takes one input parameter and returns a type of the given input such as int, string, etc.

r = range(0, 30)
print(type(r))  # Outputs <class 'range'>

print(type(10))  # Outputs <class 'int'>

print(type('a'))  # Outputs <class 'str'>

print(type("Hi there"))
